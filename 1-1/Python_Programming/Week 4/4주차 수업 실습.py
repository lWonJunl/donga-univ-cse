# if 항목 in 리스트
fruit = ["딸기"]
if '딸기' in fruit:
    print("딸기가 있네요.")
print("===================")

# if 항목 in 리스트 (없는경우)
fruit = ["딸기"]
if '바나나' not in fruit:
    print("바나나가 없네요.")
print("===================")

# for 반복문
for i in range(3):
    print("안녕하세요? for문을 공부 중입니다.")
print("===================")

# range함수 연습
for i in range(2, -1, -1):
    print("%d : 안녕하세요? for문을 공부 중입니다." % i)
print("===================")

